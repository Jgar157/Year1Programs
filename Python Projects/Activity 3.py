numHamburger = int(input("Please input the number of hamburgers."))
costHamburger = numHamburger*2
numFries = int(input("How many fries do you want?"))
costFries = numFries*1.5
numDrinks = int(input("How many drinks do you want?"))
costDrinks = numDrinks*1
totalCost = costHamburger+costFries+costDrinks
print("The total cost of your meal will be:", totalCost)
